  /**
   * Display a single order.
   * GET orders/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async show({ params, request, response, view }) {
    const limit = 1;
    const order = await Order.query()
      .orderBy("id", "desc")
      .limit(limit)
      .fetch();
    return order;
  }

