"use strict";

const Deposit = use("App/Models/Deposit");

/** @typedef {import('@adonisjs/framework/src/Request')} Request */
/** @typedef {import('@adonisjs/framework/src/Response')} Response */
/** @typedef {import('@adonisjs/framework/src/View')} View */

/**
 * Resourceful controller for interacting with deposits
 */
class DepositController {
  /**
   * Show a list of all deposits.
   * GET deposits
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async index({ request, response, view }) {
    const deposit = await Deposit.all();

    return deposit;
  }

  /**
   * Render a form to be used for creating a new deposit.
   * GET deposits/create
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async create({ request, response, view }) {}

  /**
   * Create/save a new deposit.
   * POST deposits
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async store({ request, response }) {
    const data = request.only(["id_cliente", "deposit_value", "deposit_date"]);

    const deposit = await Deposit.create(data);

    return deposit;
  }

  /**
   * Display a single deposit.
   * GET deposits/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async show({ params, request, response, view }) {
    const deposit = await Deposit.findOrFail(params.id);

    return deposit;
  }

  /**
   * Render a form to update an existing deposit.
   * GET deposits/:id/edit
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async edit({ params, request, response, view }) {}

  /**
   * Update deposit details.
   * PUT or PATCH deposits/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async update({ params, request, response }) {}

  /**
   * Delete a deposit with id.
   * DELETE deposits/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async destroy({ params, request, response }) {}
}

module.exports = DepositController;
